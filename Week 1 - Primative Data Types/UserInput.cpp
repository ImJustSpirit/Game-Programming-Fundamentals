#include <iostream>

using namespace std;

int main() {
	int numOne;
	int numTwo;

	cout << "Enter your first number: ";
	cin >> numOne;

	cout << "Enter your second number: ";
	cin >> numTwo;

	cout << numOne << " + " << numTwo << " = " << numOne + numTwo;
	cout << numOne << " * " << numTwo << " = " << numOne * numTwo;
	cout << numOne << " / " << numTwo << " = " << numOne / numTwo;
}